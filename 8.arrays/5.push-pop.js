var numbers = [1,2,3]

//push - will add the element at the end of an array
numbers.push(4)
numbers.push(5)
numbers.push(6)

// unshift - will add the element at the begining of an array
numbers.unshift(0)
numbers.unshift(-1)
numbers.unshift(-2)

// pop - will remove the element at the end of an array
numbers.pop()
numbers.pop()
numbers.pop()

// shift - will remove the element from the begining of an array
 numbers.shift()
 numbers.shift()
 numbers.shift()

console.log(numbers)